                        <a class="title"><b>PERILAKU UNIK KUCING</b></a>
                        <p> Pernahkah kalian melihat kucing yang terobsesi dengan kantong plastik dan kardus?
                            Ketika kucing melihat kantong plastik dan kardus maka kucing itu akan berusaha masuk dan
                            menyelipkan dirinya kedalam plastik atau kardus tersebut,
                            mengapa demikian? Lalu mengapa kucing suka mendengkur saat kita mengelus-elus kepalanya?
                            Lalu perilaku unik apalagi yang dimiliki oleh kucing?
                            Berikut ini adalah penjelasan dari berbagai perilaku unik kucing.
                            <br>
                            <br>
                            <b>1. Kucing mendengkur untuk meminta perhatian dan pertolongan</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/dengkuran-kucing.jpg" class="rounded" alt="...">
                            </figure><br>
                            Berbeda dengan mengeong, mungkin kamu pernah mendengar bunyi kucing yang lebih pelan seperti
                            orang mendengkur.
                            Ini adalah kebiasaan dari anak kucing yang minta disusui ibunya.
                            Kucing suka mendengkur untuk minta dibelai.
                            Getaran yang berasal dari dengkuran kucing memiliki efek menenangkan untukmu.
                            Sementara, kucing yang sakit mendengkur untuk meminta pertolongan.
                            Getaran dari dengkuran kucing berguna untuk menyembuhkan kerusakan tulang pada kucing yang
                            terluka.
                            <br>
                            <br>
                            <b>2. Kucing rumah lebih cerewet daripada kucing liar karena dia tahu bahwa mengeong adalah cara
                                terbaik untuk mendapatkan perhatianmu</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-bawel.jpg" class="rounded" alt="...">
                            </figure><br>
                            Tahukah kamu bahwa kucing gak mengeong terhadap kucing lain?
                            Kucing rajin memantau kehadiran dan kepergian kucing lain, jadi mereka gak memerlukan bahasa
                            lisan.
                            Itu hanya dilakukan oleh ibu kucing untuk berkomunikasi dengan anak kucing mereka.
                            Selain itu, kucing hanya mengeong kepada manusia karena dia tahu itu adalah cara terbaik untuk
                            mendapatkan perhatianmu.
                            <br>
                            <br>
                            <b>3. Kucing manja terhadapmu karena dia menganggapmu superior</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-caper.jpg" class="rounded" alt="...">
                            </figure><br>
                            Mungkin kamu bertanya-tanya, kenapa kucingmu begitu manja padamu?
                            Apakah dia menganggapmu sebagai orangtua, saudara atau sahabat?
                            Nyatanya, kucing yang suka menggosok-gosokkan badannya ke kakimu lebih dari menganggapmu sebagai
                            teman, tapi karena dia menganggapmu superior.
                            Ini sama seperti anak kucing menggosok-gosokkan badan ke ibunya; kucing betina ke pejantannya
                            atau kucing kecil ke kucing yang lebih besar.
                            <br>
                            <br>
                            <b>4. Kucing suka mendekatimu dengan sendirinya karena dia mencintaimu</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/poto4.jpg" class="rounded" alt="...">
                            </figure><br>
                            Tentu saja ini adalah proses alami. Gak serta-merta kamu memaksanya untuk mencintaimu.
                            Kamu akan merasakan kasih sayang kucing jika kamu memberikannya makan, merawat dan bermain
                            dengannya.
                            Selain itu, adakalanya kamu akan menemui kesulitan untuk membangun kepercayaan terhadap kucing
                            yang penakut dan gugup. Pelan-pelan saja. Cinta gak bisa dipaksakan kan?
                            <br>
                            <br>
                            <b>5. Kucing suka menggigit atau mencakar saat kamu membelainya karena dia merasa itu sudah
                                cukup</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-menggigit.jpg" class="rounded" alt="...">
                            </figure><br>
                            Kucing memang menggemaskan. Itu sebabnya kamu mungkin sangat ingin memeluk dan membelainya.
                            Saat kamu membelainya dan tiba-tiba dia menggigit dan mencakar, itu tandanya dia sudah cukup
                            untuk dibelai.
                            Sebenarnya, kucing sudah memberikan tanda-tanda sebelumnya. Saat merasa cukup, biasanya telinga
                            kucing akan merata atau ekornya sedikit berkedut.
                            Itu sebabnya, kamu kadang hanya perlu puas dengan mengelusnya sebentar dan membiarkan dia pergi
                            dari pangkuanmu.
                            <br>
                            <br>
                            <b>6. Kucing juga bisa cemburu loh!</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-ngintip.jpg" class="rounded" alt="...">
                            </figure><br>
                            Sama seperti anjing, kucing juga bisa merasa cemburu.
                            Biasanya, itu terjadi saat kamu memberikan perhatian terhadap kucing lain yang biasanya dia
                            dapatkan.
                            Meski begitu, kucing hanya merasakan cemburu dalam waktu yang singkat. Selebihnya, dia akan
                            melupakannya.
                            <br>
                            <br>
                            <b>7. Kucing bisa melupakanmu kalau kamu gak menemuinya dalam waktu yang lama</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-lupa.jpg" class="rounded" alt="...">
                            </figure><br>
                            Sedih memang. Mungkin kamu pernah pergi dalam waktu yang sangat lama lalu pulang dan mendapati
                            kucingmu merasa asing denganmu.
                            Pada dasarnya, kucing akan melupakan kucing lain jika mereka terpisah dalam waktu yang lama.
                            Hal itu pun berlaku untuk manusia. Berbeda dengan kucing, anjing dapat mengingat manusia hanya
                            dari baunya dan dapat bertahan selama berbulan-bulan, bahkan bertahun-tahun!
                            <br>
                            <br>
                            <b>8. Kucing suka memberimu tikus mati bukan sebagai hadiah</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-tangkap-tikus.jpg" class="rounded" alt="...">
                            </figure><br>
                            Biasanya, kucing akan membawa tangkapannya ke tempat yang aman sebelum memakannya.
                            Namun, jika tempat aman ini dekat dengan dapur, kucing akan menyadari bahwa makanan pemberianmu
                            lebih enak daripada tikus mati.
                            Itu sebabnya, kucing akan meninggalkan tangkapannya saat itu juga.
                            <br>
                            <br>
                            <b>9. Kucing suka menjatuhkan barang karena mencari perhatian dan atau untuk kesenangannya
                                sendiri</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-caper2.jpg" class="rounded" alt="...">
                            </figure><br>
                            Kebiasaan aneh ini mungkin sangat mengganggumu.
                            Kucing suka sekali menjatuhkan barang dari tepi rak atau meja! Namun, sebenarnya, kucing
                            melakukan itu untuk mencari perhatianmu.
                            Kadang dia juga melakukannya untuk kesenangannya sendiri sih.
                            <br>
                            <br>
                            <b>10. Kucing gak suka keluar rumah bisa jadi karena dia merasa nyaman</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-mageran.jpg" class="rounded" alt="...">
                            </figure><br>
                            Jika kamu memelihara kucing di rumah sejak dia lahir, dia gak akan pernah merasakan kehilangan
                            tinggal di luar.
                            Itu karena dia gak pernah mengalaminya.
                            Namun, lain soal jika kamu mengadopsi kucing liar dan menaruhnya dalam rumahmu.
                            Pada awalnya, dia mungkin akan merasa stres. Untuk itu, kamu bisa mengajaknya bermain agar
                            terbiasa.
                            <br>
                            <br>
                            <b>11. Kucing suka tiba-tiba berlari dan melompat dengan girang karena merasa bosan dan
                                stres</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-girang.jpg" class="rounded" alt="...">
                            </figure><br>
                            Kucing pun bisa merasa frustrasi saat bosan dan stres loh! Itu sebabnya dia suka berlari dan
                            melompat-lompat seperti orang gila.
                            Sedikit gerakan saja bisa menarik perhatiannya. Jika itu terjadi, kamu bisa loh mengajaknya
                            bermain. Hitung-hitung biar dia gak merasa bosan dan stres.
                            <br>
                            <br>
                            <b>12. Kucing suka masuk ke dalam kotak kardus karena dia merasa itu adalah tempat yang nyaman,
                                meskipun itu kardus kecil dan gak muat!</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-kardus.jpg" class="rounded" alt="...">
                            </figure><br>
                            Kucing merasa bahwa kotak kardus adalah tempat ideal dan nyaman untuk tidur.
                            Itu sebabnya, kucing sangat tergila-gila dengan kotak.
                            Meskipun, masih menjadi misteri kenapa kucing menyukai kotak kardus yang berukuran lebih kecil
                            darinya.
                            <br>
                            <br>
                            <b>13. Kucing suka memanjat dan gak bisa turun sendiri karena takut</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-manjat.jpeg" class="rounded" alt="...">
                            </figure><br>
                            Kucing memiliki cakar yang memungkinkannya untuk memanjat pohon tinggi.
                            Meski begitu, karena cakarnya memancang dan melengkung ke belakang, kucing kesulitan untuk
                            menggunakannya saat turun.
                            Itu sebabnya, dia ketakutan untuk turun.
                            Namun, sebenarnya kucing punya refleks yang bagus untuk melompat turun dengan aman. Hanya saja,
                            kucing takut.
                            <br>
                            <br>
                            <b>14 Kucing suka menjilat dan menggigit kantong plastik atau benda-benda yang bukan makanannya
                                untuk mengurangi stres</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-gigit.jpg" class="rounded" alt="...">
                            </figure><br>
                            Kantong plastik sebenarnya dianggap gak enak oleh kucing.
                            Hanya saja, dia suka menjilat dan menggigitnya untuk mengurangi stres.
                            Gak hanya kantong, kadang dia juga melakukannya pada wol atau kain.
                            Kadang, kucing pun suka memijat-mijat pada permukaan kain yang lembut dan nyaman karena dia
                            menganggapnya seperti ibunya. Bisa jadi, itu disebabkan karena anak kucing disapih terlalu dini.
                            <br>
                            <br>
                            <b>15. Kucing suka mengunyah bulunya karena merasa gatal dan ada kutu atau parasit di
                                badannya</b>
                            <br>
                            <br>
                            <figure class="img-center">
                                <img src="images/artikel2/kucing-mandi.jpg" class="rounded" alt="...">
                            </figure><br>
                            Itu adalah cara untuk menghilangkan gatal atau kutu pada tubuhnya.
                            Namun, jika dia sampai memuntahkan bulu, itu adalah tanda stres parah.
                            Bisa jadi dia mengalami konflik dengan kucing tetangga.
                        </p>
