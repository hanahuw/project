                        <a class="title"><b>10 SIFAT DAN KEPRIBADIAN KUCING MENURUT WARNA BULUNYA</b></a>
                        <p>Benarkah kucing oranye adalah kucing yang nakal seperti yang sering disebut-sebutkan
                            orang-orang belakangan ini?
                            Lalu apakah ada hubungannya antara kepribadian kucing dengan warna bulunya?
                            Hmm.. Ternyata setelah dilakukan penelitian oleh ahli genetika kucing, dokter hewan,
                            peternak kucing, ahli perilaku hewan, kepribadian kucing ada lho hubungannya dengan warna
                            bulu kucing tersebut.
                            Berikut beberapa sifat dan kepribadian kucing menurut warna bulunya:
                            <br>
                            <br>

                            <b>1. KUCING BELANG TORTOISESHELL/TORTIE</b>
                            <br> <br>
                            <figure class="img-center">
                                <img src="images/artikel1/tortoiseshell.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Kucing Tortoishell/tortie adalah kucing yang istimewa dan mempunyai karakter yang unik.
                                Mereka cenderung berkemauan keras, agak pemarah, dan mereka bisa sangat posesif terhadap
                                pemiliknya.
                                Mereka sangat mandiri, penuh semangat dan tak terduga. Mereka biasanya sangat cerewet dan
                                ingin kehadirannya disadari melalui desisan atau suara dengkurannya yang keras.
                            </p>
                            <br>
                            <b>2. KUCING HITAM</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/hitam.jpg" class="rounded" alt="...">
                            </figure>
                            <p> Bagi sebagian orang, kucing hitam dianggap pembawa sial.
                                Tapi tidak untuk pecinta kucing sejati yamg malah menganggap kucing hitam sebagai pembawa
                                keberuntungan.
                                Kucing hitam sangat pandai dan cerdas walaupun terkadang mereka penyendiri di beberapa
                                kesempatan.
                                Mereka sangat aktif dan pandai berburu. Kucing hitam sangat mudah untuk dijinakkan, mereka
                                lebih ramah dibanding kucing berwarna lain.
                                Oleh karena itu, kucing hitam yang dipelihara biasanya menjadi sangat lengket dan sangat
                                menyayangi pemiliknya.
                            </p>
                            <br>
                            <b>3. KUCING PUTIH</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/Kucing-Putih.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Walaupun tidak semua, kucing putih yang bermata biru dan odd-eyes (mata yang berlainan warna
                                kuning-biru) berisiko terkena gangguan congenital deafness alias tuli.
                                Sebagian dari mereka juga mempunyai penglihatan yang buruk.
                                Maka dari itu biasanya kucing putih bersifat pemalu dan sering mengalami gelisah.
                                Di sisi lain, kucing putih sangat manja dan senang berada di dekat pemiliknya.
                            </p>
                            <br>
                            <b>4. KUCING CALICO/BELANG TIGA (KUNING, PUTIH, HITAM)</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/calico.jpg" class="rounded" alt="...">
                            </figure>
                            <p>99,99% kucing calico atau belang tiga adalah kucing yang ber gender betina.
                                Meskipun ada yang ber gender jantan, tetapi bisa dihitung dengan jari.
                                Calico perempuan memiliki kepribadian yang cukup tenang dan santai.
                                Mereka sangat manis serta penuh kasih sayang, baik terhadap sesama kucing maupun kepada
                                manusia.
                            </p>
                            <br>
                            <b>5. KUCING ABU-ABU</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/abuabu.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Mereka sangat suka bermain dan mempunyai rasa penasaran yang sangat tinggi terhadap sesuatu.
                                Walaupun terkadang terlihat tenang dan dewasa, kucing abu-abu suka sekali mengganggu
                                kucing/peliharaan lain dengan tingkah lakunya yang nakal dan atraktif.
                            </p>
                            <br>
                            <b>6. KUCING COKLAT</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/kucing-coklat.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Kucing berwarna coklat sangat jarang ditemui.
                                Mereka cerdas, menyenangkan dan juga sangat ramah.
                                Jika dipelihara, kucing berwarna coklat ini akan lebih sering menggunakan meongan dan paws
                                nya untuk mendapatkan perhatian pemiliknya.
                            </p>
                            <br>
                            <b>7. KUCING HITAM PUTIH DAN ABU-ABU PUTIH</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/hitamputih.jpg" class="rounded" alt="...">
                            </figure>
                            <br>
                            <p>Kucing berwarna ini adalah kucing yang sangat aktif dan pandai.
                                Meskipun sedikit pemalu dan penakut, mereka pandai membuat banyak pertemanan terhadap sesama
                                kucing dan juga manusia.
                                Mereka adalah kucing yang tidak akan membuang waktu mereka hanya untuk bertengkar dengan
                                kucing lainnya.
                            </p>
                            <br>
                            <b>8. KUCING KUNING/ORANYE/GINGER</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/oren.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Jantannya bersifat tegas, agresif, mudah marah dan memiliki meongan yang cukup keras.
                                Kebalikan dari betinanya yang jauh lebih tenang dan kalem. Mereka suka dielus tetapi tidak
                                suka dipeluk/digendong.
                                Kucing oranye disebut kucing pemalas karena mereka kurang minat dalam hal berburu.
                            </p>
                            <br>
                            <b>9. KUCING “SEAL POINTS”/SIAM</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/seal-points.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Kucing ini biasa disebut kucing siamese/siam.
                                Kucing dengan spot gelap di seputaran wajah, kaki dan ekor ini sering digambarkan seperti
                                anjing.
                                Mereka semangat, pintar, penuh kasih sayang dan bisa diajak berjalan-jalan layaknya anjing.
                            </p>
                            <br>
                            <b>10. KUCING TABBY</b>
                            <br><br>
                            <figure class="img-center">
                                <img src="images/artikel1/kucing-tabby.jpg" class="rounded" alt="...">
                            </figure>
                            <p>Kucing tabby adalah kucing yang memiliki pola bulu warna belang atau garis-garis seperti
                                harimau.
                                Mereka lucu, pintar, penuh kasih sayang dan bisa menjadi sahabat yang baik bagi manusia.
                                Kucing ini biasanya cukup penyendiri dan memiliki kecenderungan untuk bermain agresif.
                                Mereka menikmati berburu mangsa mereka di luar. Oleh karena itu, cukup sulit menjaga kucing
                                tabby ini tetap berada di dalam rumah/ruangan.
                            </p>
